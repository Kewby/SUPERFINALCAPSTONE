    public StockPage(String name){
        initComponents();
        this.setLocationRelativeTo(null);
        setName(name);
        
        StockModel stm = new StockModel(); //links the stock model           
        ResultSet rs = stm.viewStock();          //calls the query from the stock model to display the products
        String val = stm.determineBranch(name);  
        
        if(val.compareTo("1")==0){
            comboBranch.setSelectedIndex(0);
            rs = stm.viewStock2("1");
        }else{
            comboBranch.setSelectedIndex(1);
            rs = stm.viewStock2("2");
        }
        
        tblStock.setModel(DbUtils.resultSetToTableModel(rs));
    }

