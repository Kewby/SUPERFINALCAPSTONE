    private void comboBranchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBranchActionPerformed
        StockModel stm = new StockModel();
        ResultSet rs = null;
        
        if(comboBranch.getSelectedItem().toString().compareTo("Cebu Branch")==0){
            rs = stm.viewStock2("1");
        }else{
            rs = stm.viewStock2("2");
        }
        
        tblStock.setModel(DbUtils.resultSetToTableModel(rs)); 

        DefaultTableModel model = (DefaultTableModel) tblStock.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel> (model);
        tblStock.setRowSorter(tr);
        try {
             tr.setRowFilter(RowFilter.regexFilter("(?i)"+searchStock.getText()));
         
            
        } catch (PatternSyntaxException e) {
            
        }
    }//GEN-LAST:event_comboBranchActionPerformed

