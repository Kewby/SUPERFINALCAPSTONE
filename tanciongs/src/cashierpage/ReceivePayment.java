    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        ReceivePaymentController rpc = new ReceivePaymentController();
            int indx,result;
            float totalP,totalC,totalt; //total purchase, total change, tender amount
            
            try {
                if(Float.parseFloat(tenderAmt.getText())!= 0){
                    totalP = getTotal(); //total purchase
                    totalt = Float.parseFloat(tenderAmt.getText()); //tender amount
                
                    if(totalP<=totalt){ //total purchase <= tender amount
                        change.setText(String.valueOf(totalt-totalP));
                        totalC = Float.parseFloat(change.getText()); //total change
                        result = tm.addToTransaction(totalt, totalC, getId(), totalP);
                        indx = tm.getLastRowId();
                        System.out.println(indx);
                        rpc.getOrder(indx, table);
                        table.setRowCount(0);
                    
                        if(result>0){
                            getgTotal().setText("0.00"); //restart the total to 0.00 for a new transation in CASHIER PAGE
                            getLblItems().setText("0"); //number of items below the table
                            this.dispose();
                    }
                    
                }else{
                     JOptionPane.showMessageDialog(frame, "Insufficient Funds.");
                }
                
            }else{
                JOptionPane.showMessageDialog(frame, "You should Input payment");
            }
            }catch(NumberFormatException n){
                JOptionPane.showMessageDialog(frame, "You should input a number ");
            }
    }//GEN-LAST:event_btnConfirmActionPerformed

